    @Test
    @Transactional
    public void updateCurrency() throws Exception {
        // Initialize the database
        currencyRepository.saveAndFlush(currency);

        int databaseSizeBeforeUpdate = currencyRepository.findAll().size();

        // Update the currency
        Currency updatedCurrency = currencyRepository.findById(currency.getId()).get();
        // Disconnect from session so that the updates on updatedCurrency are not directly saved in db
        em.detach(updatedCurrency);
        updatedCurrency
            .title(UPDATED_TITLE)
            .code(UPDATED_CODE)
            .symbolLeft(UPDATED_SYMBOL_LEFT)
            .symbolRight(UPDATED_SYMBOL_RIGHT)
            .decimalPlace(UPDATED_DECIMAL_PLACE)
            .value(UPDATED_VALUE)
            .isActive(UPDATED_IS_ACTIVE)
            .createdBy(UPDATED_CREATED_BY)
            .modifiedBy(UPDATED_MODIFIED_BY)
            .createdDate(UPDATED_CREATED_DATE)
            .modifiedDate(UPDATED_MODIFIED_DATE);
        CurrencyDTO currencyDTO = currencyMapper.toDto(updatedCurrency);

        restCurrencyMockMvc.perform(put("/api/currencies")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(currencyDTO)))
            .andExpect(status().isOk());

        // Validate the Currency in the database
        List<Currency> currencyList = currencyRepository.findAll();
        assertThat(currencyList).hasSize(databaseSizeBeforeUpdate);
        Currency testCurrency = currencyList.get(currencyList.size() - 1);
        assertThat(testCurrency.getTitle()).isEqualTo(UPDATED_TITLE);
        assertThat(testCurrency.getCode()).isEqualTo(UPDATED_CODE);
        assertThat(testCurrency.getSymbolLeft()).isEqualTo(UPDATED_SYMBOL_LEFT);
        assertThat(testCurrency.getSymbolRight()).isEqualTo(UPDATED_SYMBOL_RIGHT);
        assertThat(testCurrency.getDecimalPlace()).isEqualTo(UPDATED_DECIMAL_PLACE);
        assertThat(testCurrency.getValue()).isEqualTo(UPDATED_VALUE);
        assertThat(testCurrency.getIsActive()).isEqualTo(UPDATED_IS_ACTIVE);
        assertThat(testCurrency.getCreatedBy()).isEqualTo(UPDATED_CREATED_BY);
        assertThat(testCurrency.getModifiedBy()).isEqualTo(UPDATED_MODIFIED_BY);
        assertThat(testCurrency.getCreatedDate()).isEqualTo(UPDATED_CREATED_DATE);
        assertThat(testCurrency.getModifiedDate()).isEqualTo(UPDATED_MODIFIED_DATE);
    }

