    @Test
    @Transactional
    public void createCurrency() throws Exception {
        int databaseSizeBeforeCreate = currencyRepository.findAll().size();

        // Create the Currency
        CurrencyDTO currencyDTO = currencyMapper.toDto(currency);
        restCurrencyMockMvc.perform(post("/api/currencies")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(currencyDTO)))
            .andExpect(status().isCreated());

        // Validate the Currency in the database
        List<Currency> currencyList = currencyRepository.findAll();
        assertThat(currencyList).hasSize(databaseSizeBeforeCreate + 1);
        Currency testCurrency = currencyList.get(currencyList.size() - 1);
        assertThat(testCurrency.getTitle()).isEqualTo(DEFAULT_TITLE);
        assertThat(testCurrency.getCode()).isEqualTo(DEFAULT_CODE);
        assertThat(testCurrency.getSymbolLeft()).isEqualTo(DEFAULT_SYMBOL_LEFT);
        assertThat(testCurrency.getSymbolRight()).isEqualTo(DEFAULT_SYMBOL_RIGHT);
        assertThat(testCurrency.getDecimalPlace()).isEqualTo(DEFAULT_DECIMAL_PLACE);
        assertThat(testCurrency.getValue()).isEqualTo(DEFAULT_VALUE);
        assertThat(testCurrency.getIsActive()).isEqualTo(DEFAULT_IS_ACTIVE);
        assertThat(testCurrency.getCreatedBy()).isEqualTo(DEFAULT_CREATED_BY);
        assertThat(testCurrency.getModifiedBy()).isEqualTo(DEFAULT_MODIFIED_BY);
        assertThat(testCurrency.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
        assertThat(testCurrency.getModifiedDate()).isEqualTo(DEFAULT_MODIFIED_DATE);
    }

